function fig = plot_proximity_guidance_trajectory_2d(simulation)
% plot_proximity_guidance_trajectory_2d 绘制相对系中的分段近距离导引轨迹。

arguments
    simulation (1,1) struct
end

fig = utils.createFigureA4(struct( ...
    'Name', '近距离导引相对系轨迹', ...
    'Width', 20, ...
    'AspectRatio', 0.75));
ax = axes('Parent', fig);
hold(ax, 'on');
grid(ax, 'on');
box(ax, 'on');

theta_full = linspace(0, 2 * pi, 360);
[rel_r_ref_full, ~] = proximity_reference_orbit_state_2d( ...
    simulation.target.n, simulation.plan.orbit, theta_full);
ref_line = [simulation.initial_relative.rel_r0, simulation.plan.rel_rf];

plot(ax, rel_r_ref_full(1, :) / 1e3, ...
    rel_r_ref_full(2, :) / 1e3, ...
    ':', 'Color', [0.45, 0.45, 0.45], 'LineWidth', 1.2, ...
    'DisplayName', utils.formatMixedFontText('绕飞轨道'));
plot(ax, ref_line(1, :) / 1e3, ...
    ref_line(2, :) / 1e3, ...
    '--', 'Color', [0.55, 0.55, 0.55], 'LineWidth', 1.2, ...
    'HandleVisibility', 'off');

for k = 1:numel(simulation.segments)
    display_name = utils.formatMixedFontText('数值转移轨迹');
    handle_visibility = 'on';
    if k > 1
        display_name = '';
        handle_visibility = 'off';
    end

    plot(ax, simulation.segments(k).rel_r_numeric(1, :) / 1e3, ...
        simulation.segments(k).rel_r_numeric(2, :) / 1e3, ...
        '-', ...
        'Color', [0.10, 0.35, 0.85], ...
        'LineWidth', 1.8, ...
        'DisplayName', display_name, ...
        'HandleVisibility', handle_visibility);
end

if size(simulation.post_insert.rel_r_numeric, 2) >= 2
    plot(ax, simulation.post_insert.rel_r_numeric(1, :) / 1e3, ...
        simulation.post_insert.rel_r_numeric(2, :) / 1e3, ...
        '-', ...
        'Color', [0.12, 0.62, 0.28], ...
        'LineWidth', 1.8, ...
        'DisplayName', utils.formatMixedFontText('数值绕飞轨迹'));
end

plot(ax, simulation.plan.rel_r_seq(1, :) / 1e3, ...
    simulation.plan.rel_r_seq(2, :) / 1e3, ...
    's', 'MarkerSize', 5.5, ...
    'MarkerFaceColor', [0.98, 0.75, 0.20], ...
    'MarkerEdgeColor', 'k', ...
    'LineStyle', 'none', ...
    'DisplayName', utils.formatMixedFontText('脉冲节点'));
plot(ax, simulation.plan.rel_r_seq(1, 1) / 1e3, ...
    simulation.plan.rel_r_seq(2, 1) / 1e3, ...
    'o', 'MarkerSize', 6.5, ...
    'MarkerFaceColor', [0.10, 0.35, 0.85], ...
    'MarkerEdgeColor', 'k', ...
    'DisplayName', utils.formatMixedFontText('初始点'));
plot(ax, simulation.plan.rel_r_seq(1, end) / 1e3, ...
    simulation.plan.rel_r_seq(2, end) / 1e3, ...
    'd', 'MarkerSize', 6.5, ...
    'MarkerFaceColor', [0.15, 0.65, 0.35], ...
    'MarkerEdgeColor', 'k', ...
    'DisplayName', utils.formatMixedFontText('切入点'));
plot(ax, 0, 0, ...
    'p', 'MarkerSize', 8.0, ...
    'MarkerFaceColor', [0.85, 0.20, 0.20], ...
    'MarkerEdgeColor', 'k', ...
    'DisplayName', utils.formatMixedFontText('目标飞行器'));

x_main_km = [simulation.relative.numeric_r(1, :), ...
    simulation.plan.rel_r_seq(1, :), ...
    ref_line(1, :), ...
    0] / 1e3;
y_main_km = [simulation.relative.numeric_r(2, :), ...
    simulation.plan.rel_r_seq(2, :), ...
    ref_line(2, :), ...
    0] / 1e3;
x_range_km = [min(x_main_km), max(x_main_km)];
y_range_km = [min(y_main_km), max(y_main_km)];
pad_x = max(0.3, 0.12 * max(diff(x_range_km), 1e-6));
pad_y = max(0.3, 0.12 * max(diff(y_range_km), 1e-6));

xlim(ax, x_range_km + [-pad_x, pad_x]);
ylim(ax, y_range_km + [-pad_y, pad_y]);
xlabel(ax, utils.formatMixedFontText('LVLH x (km)'));
ylabel(ax, utils.formatMixedFontText('LVLH y (km)'));
title(ax, utils.formatMixedFontText('近距离导引与切入后绕飞轨迹'));
legend(ax, 'Location', 'bestoutside');

end
