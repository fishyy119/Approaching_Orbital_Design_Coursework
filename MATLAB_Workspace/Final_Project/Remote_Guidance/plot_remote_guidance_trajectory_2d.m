function fig = plot_remote_guidance_trajectory_2d(simulation)
% plot_remote_guidance_trajectory_2d 绘制最优远程导引轨迹，并区分等待段与转移段。
%
% 输入：
%   simulation    远程导引数值仿真结果结构体。
%
% 输出：
%   fig           轨迹图图窗句柄。

arguments
    simulation (1,1) struct {mustBeRemoteGuidanceSimulation}
end

fig = utils.createFigureA4(struct( ...
    'Name', '最优 Lambert 转移轨迹', ...
    'Width', 20, ...
    'AspectRatio', 0.75));
ax = axes('Parent', fig);
hold(ax, 'on');
grid(ax, 'on');
axis(ax, 'equal');
box(ax, 'on');

theta = linspace(0, 2 * pi, 400);
fill(ax, simulation.Re * cos(theta) / 1e3, simulation.Re * sin(theta) / 1e3, ...
    [0.92, 0.94, 0.98], ...
    'EdgeColor', [0.55, 0.60, 0.70], ...
    'HandleVisibility', 'off');

plot(ax, simulation.reference.target_pos(1, :) / 1e3, ...
    simulation.reference.target_pos(2, :) / 1e3, ...
    ':', 'Color', [0.80, 0.25, 0.25], 'LineWidth', 1.0, ...
    'HandleVisibility', 'off');
plot(ax, simulation.reference.chaser_pos(1, :) / 1e3, ...
    simulation.reference.chaser_pos(2, :) / 1e3, ...
    ':', 'Color', [0.55, 0.55, 0.55], 'LineWidth', 1.0, ...
    'HandleVisibility', 'off');

plot(ax, simulation.mission.target_pos(1, :) / 1e3, ...
    simulation.mission.target_pos(2, :) / 1e3, ...
    '-.', 'Color', [0.85, 0.20, 0.20], 'LineWidth', 1.4, ...
    'DisplayName', utils.formatMixedFontText('目标飞行段'));
plot(ax, simulation.wait.chaser_pos(1, :) / 1e3, ...
    simulation.wait.chaser_pos(2, :) / 1e3, ...
    '--', 'Color', [0.10, 0.35, 0.85], 'LineWidth', 1.8, ...
    'DisplayName', utils.formatMixedFontText('等待段'));
plot(ax, simulation.transfer.chaser_pos(1, :) / 1e3, ...
    simulation.transfer.chaser_pos(2, :) / 1e3, ...
    '-', 'Color', [0.10, 0.35, 0.85], 'LineWidth', 2.0, ...
    'DisplayName', utils.formatMixedFontText('转移段'));

plot(ax, simulation.events.start(1) / 1e3, simulation.events.start(2) / 1e3, ...
    'o', 'MarkerSize', 6.5, ...
    'MarkerFaceColor', [0.10, 0.35, 0.85], ...
    'MarkerEdgeColor', 'k', ...
    'DisplayName', utils.formatMixedFontText('初始点'));
plot(ax, simulation.events.departure(1) / 1e3, simulation.events.departure(2) / 1e3, ...
    's', 'MarkerSize', 6.5, ...
    'MarkerFaceColor', [0.98, 0.75, 0.20], ...
    'MarkerEdgeColor', 'k', ...
    'DisplayName', utils.formatMixedFontText('转移起点'));
plot(ax, simulation.events.arrival_capture(1) / 1e3, ...
    simulation.events.arrival_capture(2) / 1e3, ...
    'd', 'MarkerSize', 7.0, ...
    'MarkerFaceColor', [0.15, 0.65, 0.35], ...
    'MarkerEdgeColor', 'k', ...
    'DisplayName', utils.formatMixedFontText('捕获点'));

xlim(ax, simulation.axis_limit_km * [-1, 1]);
ylim(ax, simulation.axis_limit_km * [-1, 1]);
xlabel(ax, utils.formatMixedFontText('惯性系 {\itx} (km)'));
ylabel(ax, utils.formatMixedFontText('惯性系 {\ity} (km)'));
title(ax, utils.formatMixedFontText('最优 Lambert 窗口对应轨迹'));
legend(ax, 'Location', 'bestoutside');

end
