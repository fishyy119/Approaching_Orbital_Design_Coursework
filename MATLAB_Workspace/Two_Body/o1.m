%% 霍曼转移1
%
% 计算航天器从圆轨道到圆轨道的霍曼转移所需速度增量。

clc;
clear;

%% 常数
R_e = 6378137; % 地球半径，单位 m
mu_e = 3.986004418e14; % 地球引力参数，单位 m^3/s^2
h_1 = 400e3; % 初始轨道高度，单位 m
h_2 = 600e3; % 目标轨道高度，单位 m

a_1 = R_e + h_1; % 初始轨道半径
a_2 = R_e + h_2; % 目标轨道半径
a_e = (a_1 + a_2) / 2; % 霍曼转移轨道半长轴

%% 速度计算
v_1 = ra2v(a_1, a_1, mu_e); % 初始圆轨道速度
v_2 = ra2v(a_2, a_2, mu_e); % 目标圆轨道速度

v_12 = ra2v(a_1, a_e, mu_e); % 转移轨道起点速度
v_21 = ra2v(a_2, a_e, mu_e); % 转移轨道终点速度

%% 速度增量
d1 = v_12 - v_1; % 第一次脉冲
d2 = v_2 - v_21; % 第二次脉冲
fprintf('dv_1 = %.6f m/s\n', d1)
fprintf('dv_2 = %.6f m/s\n', d2)
fprintf('sum = %.6f m/s\n', abs(d1) + abs(d2))
